/* ─── Format helpers ────────────────────────────────────────────────────── */
export function fmtUptime(secs) {
  const h = Math.floor(secs / 3600);
  const m = Math.floor((secs % 3600) / 60);
  const s = secs % 60;
  if (h > 0) return `${h}h ${m}m`;
  if (m > 0) return `${m}m ${s}s`;
  return `${s}s`;
}

export function fmtSize(bytes) {
  if (!bytes) return '';
  if (bytes < 1024) return `${bytes}B`;
  if (bytes < 1048576) return `${(bytes / 1024).toFixed(0)}KB`;
  return `${(bytes / 1048576).toFixed(1)}MB`;
}

export function fmtDate(iso) {
  if (!iso) return '—';
  return new Date(iso).toLocaleString();
}

export function timeAgo(iso) {
  if (!iso) return 'never';
  // Ensure UTC parsing — append Z if no timezone info present
  const normalized = /[Z+\-]\d*$/.test(iso.trim()) ? iso : iso + 'Z';
  const secs = Math.floor((Date.now() - new Date(normalized).getTime()) / 1000);
  if (secs < 5)  return 'just now';
  if (secs < 60) return `${secs}s ago`;
  const mins = Math.floor(secs / 60);
  if (mins < 60) return `${mins}m ago`;
  const hrs = Math.floor(mins / 60);
  if (hrs < 24)  return `${hrs}h ago`;
  const days = Math.floor(hrs / 24);
  return `${days}d ago`;
}

/* ─── Log line colorizer ─────────────────────────────────────────────────── */
export function logClass(line) {
  if (/error|fail|fatal|exception/i.test(line)) return 'log-error';
  if (/warn/i.test(line)) return 'log-warn';
  if (/success|ready|started|listening|connected/i.test(line)) return 'log-success';
  if (/info|debug/i.test(line)) return 'log-info';
  return 'log-default';
}

/* ─── setBtn helper ─────────────────────────────────────────────────────── */
export function setBtn(btn, loading, label, iconHtml) {
  if (loading) {
    btn.disabled = true;
    btn.innerHTML = `${spinner} ${label}`;
  } else {
    btn.disabled = false;
    btn.innerHTML = `${iconHtml} ${label}`;
  }
}
